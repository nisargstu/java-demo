package Patterns;

public class pat4 {
    public static void main(String[] args) {
        //1
        //2
        //3
        //4
        //54321

        int n=4;
        int m=0;
        for(int i=1; i<=n; i++)
        {
            System.out.println(i);
        }
        for (int j=5; j>m; j--)
        {
            System.out.print(j);
        }
    }
}
